def find_element_index(array, value_to_find)
  # so if you pass in [1, 2, 3, 4, 5] it should return 2 if you pass in array, 3 #Pseudocode
  if array.each |x| = value_to_find 
    return array(x)
  else
    return nil
end
end





def find_max_value(array)
  return array.max
end

def find_min_value(array)
  return array.min
end
